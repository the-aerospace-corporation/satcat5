static void macsec_unit_test(unsigned int key_len, const u8* key, const u8* iv,
    u8 tci, u64 sci, u64 pn, u64 ssci, bool xpn,
    const test_frame& raw_frame, const test_frame& sec_frame)
{
    // Create the unit under test.
    satcat5::eth::MacSec uut = xpn
        ? satcat5::eth::MacSec(key_len, key, iv, tci, sci, ssci)
        : satcat5::eth::MacSec(key_len, key, tci, sci);

    // Create working buffers.
    satcat5::io::ArrayRead ref_raw(raw_frame.len, raw_frame.data);
    satcat5::io::ArrayRead ref_sec(sec_frame.len, sec_frame.data);
    satcat5::io::PacketBufferHeap buff;

    // Encrypt original and compare to the secure reference.
    CHECK(uut.encrypt_frame(&ref_raw, &buff, pn));
    CHECK(satcat5::test::read_equal(&buff, &ref_sec));
    ref_raw.read_finalize();

    // Decrypt secure reference and compare to the original.
    CHECK(uut.decrypt_frame(&ref_sec, &buff, pn));
    CHECK(satcat5::test::read_equal(&buff, &ref_raw));
    ref_sec.read_finalize();
}

